import React from 'react';
import {
  SEND_SEARCH_REQUEST,
  SET_TRANSPORTATION_STATUS,
} from '../actions/actionTypes';

export const initialState = {
  responce_data: false,
  transportation_status: 'sea',
};

export const filterReducer = (state = initialState, action) => {
  switch (action.type) {
    case SEND_SEARCH_REQUEST:
      return {
        ...state,
        ...action.playload,
      };
      break;
    case SET_TRANSPORTATION_STATUS:
      return {
        ...state,
        ...action.playload,
      };
      break;
    default:
      return { ...state };
  }
};
